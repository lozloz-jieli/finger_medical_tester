/**************************************************************
函数名称 : LCD_Clear
函数功能 : lcd清屏函数
输入参数 : color：要清屏的颜色
		   on_off:是否打开或关闭屏
返回值  	 : 无
备注		 : 先关闭显示，等到所有颜色数据写入到RAM后再打开显示
**************************************************************/
u8 the_pic_buff[160*8*10*2]={0};

void LCD_Clear(u16 color)
{
    u16 i, j;

	//lcd_display_off();		/* 关闭显示 */
	SetAddress(0, 0, ROW - 1, COL - 1);

	for(i = 0; i < COL; i++)
	{
		for(j = 0; j < ROW; j++)
		{
			SPI_WriteData_Da(color >> 8);
			SPI_WriteData_Da(color & 0x00ff);
		}
	}

	//lcd_display_on();		/* 打开显示 */
}

void LCD_DMA_Clear(u16 color)
{
    u16 i, j;
    u16 b_l=0;

	//lcd_display_off();		/* 关闭显示 */
	// SetAddress(0, 0, ROW - 1, COL - 1);
	BlockWrite(26,COL+25,1,ROW);

	LCD_CS_SLECT_ENABLE(); 
	LCD_RS_DATA();

	for(i = 0; i < COL; i++)
	{
		for(j = 0; j < ROW; j++)
		{
			the_pic_buff[b_l++] = (color >> 8);
			the_pic_buff[b_l++] = (color & 0x00ff);
		}
	}
    // printf("LCD_DMA_Clear b_l = %d",b_l);

    spi_dma_send(lcd_spi1_hdl,the_pic_buff, b_l);

	//lcd_display_on();		/* 打开显示 */
}


