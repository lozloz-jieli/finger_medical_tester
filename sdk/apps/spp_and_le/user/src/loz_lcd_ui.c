void DrawString(uint16_t x, uint16_t y, const uint16_t *string, uint16_t color, uint16_t bgColor) {
    // 绘制第一个字符
    SetAddress(x, y, 16, 16);  // 设置显示区域
    Draw16x16Char(x, y, string, color, bgColor); 

    // 绘制第二个字符
    SetAddress(x + 16, y, 16, 16);  // 设置下一个字符的位置
    Draw16x16Char(x + 16, y, string + 16, color, bgColor);

    // 绘制第三个字符
    SetAddress(x + 32, y, 16, 16);  // 设置下一个字符的位置 
    Draw16x16Char(x + 32, y, string + 32, color, bgColor);
}



